# 题目：爬楼梯（字节高频版）
# 题目描述：
# 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
# 每次你可以爬 1 个台阶，或者 2 个台阶。但是不能连续两次爬 2 个台阶。
# 问有多少种不同的方法可以爬到楼顶？

# 状态定义
# dp[i][0]代表爬到第i阶时最后一步走的一个台阶的方法数
# dp[i][0]代表爬到第i阶时最后一步走的两个台阶的方法数
# 状态转移
# 想走第i阶且最后一步走的是1步，那么dp[i][0] = dp[i-1][1]+dp[i-1][0]
# 想走第i阶且最后一步走的是2步，那么dp[i][1] = dp[i-2][0]
# 初始化
# dp[1][0] = 1（1 阶只能走 1 步），dp[1][1] = 0（1 阶走不了 2 步）；
# dp[2][0] = dp[1][0] + dp[1][1] = 1（2 阶最后走 1 步：先到 1 阶再走 1）；
# dp[2][1] = dp[0][0]（补一个 dp [0][0]=1，代表 “0 阶不用走” 的初始状态）→ dp[2][1] = 1；

def climbStairs(n):
    # 处理边界情况
    if n == 1:
        return 1
    if n == 2:
        return 2  # 1+1 或 2
    
    # 初始化dp数组，dp[i][0]最后一步走1，dp[i][1]最后一步走2
    dp = [[0] * 2 for _ in range(n+1)]
    dp[1][0] = 1
    dp[1][1] = 0
    dp[0][0] = 1  # 虚拟初始状态：0阶有1种方法（不走）
    
    for i in range(2, n+1):
        dp[i][0] = dp[i-1][0] + dp[i-1][1]
        # 走2步到i，必须从i-2阶走过来，且i-2最后一步是1
        dp[i][1] = dp[i-2][0]
    
    return dp[n][0] + dp[n][1]

# 测试用例
print(climbStairs(3))  # 输出：3（1+1+1、1+2、2+1 → 注意2+1合法，1+2合法，不能2+2）
print(climbStairs(4))  # 输出：4（1+1+1+1、1+1+2、1+2+1、2+1+1）